import type { OpportunityRow } from './types'
import type { Opportunity, OpportunityListItem } from '@/types/opportunity'

const LIST_ITEM_SELECT =
  "id, slug, name, pitch, origin_country, origin_country_code, origin_flag, sector, target_client, client_type, tech_complexity, france_competition, revenue_min, revenue_max, buildable_under_30_days, weekly_pick, published_at, created_at, scores"

export const OPPORTUNITY_LIST_SELECT = LIST_ITEM_SELECT

export function mapRowToOpportunity(row: OpportunityRow): Opportunity {
  return {
    id: row.id,
    slug: row.slug,
    name: row.name,
    pitch: row.pitch,
    originCountry: row.origin_country,
    originCountryCode: row.origin_country_code,
    originFlag: row.origin_flag,
    sector: row.sector as Opportunity['sector'],
    targetClient: row.target_client,
    clientType: row.client_type as Opportunity['clientType'],
    techComplexity: row.tech_complexity as Opportunity['techComplexity'],
    franceCompetition: row.france_competition as Opportunity['franceCompetition'],
    revenueMin: row.revenue_min,
    revenueMax: row.revenue_max,
    buildableUnder30Days: row.buildable_under_30_days,
    boringBusiness: row.boring_business,
    aiPowered: row.ai_powered,
    lowCompetition: row.low_competition,
    weeklyPick: row.weekly_pick,
    entrepreneursBuilding: row.entrepreneurs_building,
    foreignInspiration: row.foreign_inspiration,
    claudePrompt: row.claude_prompt,
    url: row.url ?? undefined,
    createdAt: row.created_at,
    publishedAt: row.published_at ?? undefined,
    scores: row.scores as unknown as Opportunity['scores'],
    franceFitCriteria: row.france_fit_criteria as unknown as Opportunity['franceFitCriteria'],
    tractionSignals: row.traction_signals as unknown as Opportunity['tractionSignals'],
    whyItWorks: row.why_it_works as unknown as Opportunity['whyItWorks'],
    franceAnalysis: row.france_analysis as string[],
    financialScenarios: row.financial_scenarios as unknown as Opportunity['financialScenarios'],
    cacChannels: row.cac_channels as unknown as Opportunity['cacChannels'],
    acquisition: row.acquisition as unknown as Opportunity['acquisition'],
    mvpPlan: row.mvp_plan as unknown as Opportunity['mvpPlan'],
    foreignMarketProfile: (row.foreign_market_profile as unknown as Opportunity['foreignMarketProfile']) ?? undefined,
    infraCosts: (row.infra_costs as unknown as Opportunity['infraCosts']) ?? undefined,
    frenchCompetitors: (row.french_competitors as unknown as Opportunity['frenchCompetitors']) ?? undefined,
    launchTimeline: (row.launch_timeline as unknown as Opportunity['launchTimeline']) ?? undefined,
    emailTemplates: (row.email_templates as unknown as Opportunity['emailTemplates']) ?? undefined,
    partnersFR: (row.partners_fr as unknown as Opportunity['partnersFR']) ?? undefined,
    roiInputs: (row.roi_inputs as unknown as Opportunity['roiInputs']) ?? undefined,
    tamBreakdown: (row.tam_breakdown as unknown as Opportunity['tamBreakdown']) ?? undefined,
    competitionAlerts: (row.competition_alerts as unknown as Opportunity['competitionAlerts']) ?? undefined,
  }
}

